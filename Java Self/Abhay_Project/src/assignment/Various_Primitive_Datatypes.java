package assignment;

public class Various_Primitive_Datatypes {
	 
	 public static void main(String[] args) {
		 Boolean v = true;
		 char a = 'M';
		 byte r = 10;
		 int i = 20;
		 short o = 200;
		 long u = 6262173362L;
		 float s = 50.50f;
		 double data = 99.9999d;
		 System.out.println("Boolean: " + v);
		 System.out.println("char: " + a);
		 System.out.println("Byte: " + r);
		 System.out.println("Integer: " + i);
		 System.out.println("short: " + o);
		 System.out.println("Long: " + u);
		 System.out.println("Float: " + s);
		 System.out.println("Double: " + data);

	}
}
